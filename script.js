document.addEventListener('DOMContentLoaded', function() {
    // Localization state
    let currentLang = localStorage.getItem('nazel_lang') || 'ar';

    // Initialize AOS Animation Library
    AOS.init({
        duration: 800,
        easing: 'ease-in-out',
        once: true,
        offset: 100
    });

    // Language Switching Logic
    const langBtn = document.getElementById('lang-switcher');
    const langText = langBtn.querySelector('.lang-text');

    function updateContent() {
        // Update document attributes
        document.documentElement.lang = currentLang;
        document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
        
        // Update button text
        langText.textContent = currentLang === 'ar' ? 'EN' : 'AR';

        // Update all elements with data-i18n
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[currentLang][key]) {
                el.innerHTML = translations[currentLang][key];
            }
        });

        // Update elements with data-i18n-attr (for meta descriptions, titles, etc)
        document.querySelectorAll('[data-i18n-attr]').forEach(el => {
            const attrData = el.getAttribute('data-i18n-attr').split(':');
            const attrName = attrData[0];
            const key = attrData[1];
            if (translations[currentLang][key]) {
                el.setAttribute(attrName, translations[currentLang][key]);
            }
        });

        // Repopulate year after content update if it was overwritten
        const yearEl = document.getElementById('year');
        if (yearEl) {
            yearEl.textContent = new Date().getFullYear();
        }

        // Refresh AOS to handle layout changes
        AOS.refresh();
    }

    langBtn.addEventListener('click', () => {
        currentLang = currentLang === 'ar' ? 'en' : 'ar';
        localStorage.setItem('nazel_lang', currentLang);
        updateContent();
    });

    // Initial content update
    updateContent();

    // Navbar Scrolled Effect
    const navbar = document.querySelector('.navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // Accordion Logic
    const accordionHeaders = document.querySelectorAll('.accordion-header');

    accordionHeaders.forEach(header => {
        header.addEventListener('click', () => {
            const item = header.parentElement;
            
            // Check if already active
            const isActive = item.classList.contains('active');
            
            // Close all items
            document.querySelectorAll('.accordion-item').forEach(accItem => {
                accItem.classList.remove('active');
            });

            // If it wasn't active, open it
            if (!isActive) {
                item.classList.add('active');
            }
        });
    });
});

